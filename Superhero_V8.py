heroes = {}

for i in range (2):
    hero_ID = input("\n\n Enter ID: ")
    heroes[hero_ID] = {}\

    name = input("\n\n Enter Name: ")
    heroes[name] = name

    power = input("\n\n Enter Power: ")
    heroes[power] = power

    strength = input("\n\n Enter strength: ")
    heroes[strength] = strength



